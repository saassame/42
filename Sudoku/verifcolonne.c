int ft_verifcolone(char **argv, int ch, int p)
{
	int i;

	i = 1;
	while ((argv[ch][p] != argv[i][p]) || (ch == i))
	{
		if (i == 10)
		{
			return (1);
		}
		i++;
	}
	return (0);
}
