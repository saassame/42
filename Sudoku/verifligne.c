int		ft_verifligne(char **argv, int ch, int p)
{
	int	i;

	i = 0;
	while ((argv[ch][p] != argv[ch][i]) || (p == i))
	{
		if (i == 9)
		{
			return (1);
		}
		i++;
	}
	return (0);
}
