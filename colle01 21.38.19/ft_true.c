int	ft_true(char **argv)
{
	int i;
	int j;
	int flag;

	i = 0;
	flag = 0;
	while (i < 9)
	{
		j = 1;
		while (argv[i][j])
			j++;
		if (j == 9)
			flag = 1;
		i++;
	}
	return (flag);
}
