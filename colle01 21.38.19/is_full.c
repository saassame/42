int	is_full(int sudoku[9][9])
{
	int x;
	int y;

	y = 0;
	x = 0;
	while (y < 9)
	{
		while (x < 9)
		{
			if (sudoku[y][x] == 0)
				return (0);
			x++;
		}
		x = 0;
		y++;
	}
	return (1);
}
